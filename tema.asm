MOV DPTR, #TAB

MAIN:
	CALL BYTEANIM	
	CALL SEGANIM
JMP MAIN

SEGANIM:
	MOV R3, #4D
INN4:	MOV A, R3
	DEC A
	MOVC A, @A+DPTR
	MOV P1, A
	CALL DEL550MS
	DJNZ R3, INN4

BYTEANIM:
	MOV R4, #16D
	MOV A, #11111110B
INN3:
	MOV P0, A
	RL A
	CALL DEL480MS
	DJNZ R4, INN3
	RET

DEL480MS:
	MOV R2, #8D
INN1:	MOV R1, #21D
	MOV R0, #170D
	CALL DELVAR
	DJNZ R2, INN1
	RET

DEL550MS:
	MOV R2, #10D
INN2:	MOV R1, #41D
	MOV R0, #50D
	CALL DELVAR
	DJNZ R2, INN2
	RET

DELVAR:
	MOV TMOD, #1B
	MOV TH0, R1
	MOV TL0, R0
	CLR TF0
	
	SETB TR0

	JNB TF0, $
	
	RET

TAB:	DB 11111001B
	DB 01100100B;
	DB 01110000B
	DB 01011001B
	;db 00111111B ;0
	;db 00000110B 
	;db 01011011B
	;db 01001111B
	;db 01100110B
	;db 01101101B
	;db 01111101B
	;db 00000111B
	;db 01111111B 
	;db 01101111B ;9
END